/**
 * Compact VRF badge for group cards
 */
export function VRFBadge({ useVRF }: { useVRF: boolean }) {
  if (!useVRF) return null;

  return (
    <div className="tooltip tooltip-right" data-tip="Fair random payout order using Chainlink VRF">
      <div className="badge badge-success gap-1">
        <span>üé≤</span>
        <span>Fair Random</span>
      </div>
    </div>
  );
}

/**
 * VRF request status indicator
 */
export function VRFStatusIndicator({ fulfilled, pending }: { fulfilled: boolean; pending: boolean }) {
  if (fulfilled) {
    return (
      <div className="flex items-center gap-2 text-green-600">
        <span className="text-xl">‚úì</span>
        <span className="font-semibold">VRF Fulfilled</span>
      </div>
    );
  }

  if (pending) {
    return (
      <div className="flex items-center gap-2 text-orange-600">
        <span className="loading loading-spinner loading-sm"></span>
        <span className="font-semibold">Awaiting VRF...</span>
      </div>
    );
  }

  return (
    <div className="flex items-center gap-2 text-gray-500">
      <span>üé≤</span>
      <span>VRF Not Requested</span>
    </div>
  );
}
